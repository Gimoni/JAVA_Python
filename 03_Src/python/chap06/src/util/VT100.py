# 라이브러리 함수정의만 저장. 

def clearScreen():
    print("\033[2J", end='', flush=True)
    pass

def cursorMove(line, column):
    print(f"\033[{line};{column}H", end='')
    pass

def setForeground(fg):
    print(f"\033[{fg}m", end='')
    pass

def setBackground(bg):
    print(f"\033[{bg}m", end='')
    pass

def reset():
    print(f"\033[0m", end='', flush=True)
    pass







