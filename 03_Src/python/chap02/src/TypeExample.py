# 정수    int
# 실수    float
# 논리    bool    
# 문자열  str ==> python은 문자(char)타입이 없다.



b1 = 100
s1 = 200
c1 = 'A'    # 문자열
i1 = 300
l1 = 400 

f1 = 10.5
d1 = 10.5

tf = True

print("b1 = ", type(b1))    # int
print("s1 = ", type(s1))    # int
print("c1 = ", type(c1))    # str
print("i1 = ", type(i1))    # int
print("l1 = ", type(l1))    # int

print("f1 = ", type(f1))    # float
print("d1 = ", type(d1))    # float

print("tf = ", type(tf))    # bool

