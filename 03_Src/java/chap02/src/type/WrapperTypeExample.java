package type;

public class WrapperTypeExample {

	public static void main(String[] args) {
		// Reference type
		// wrapper type = Primitive type to Reference type 
		Byte b1 = 100;
		Short s1 = 200;
	    Character  c1 = 'A';
		Integer i1 = 300;
		Long l1 = 400L;
		
		Float f1 = 10.5f;
		Double d1 = 10.5;
		
		Boolean tf = true;
		
	}
	
	public static void main2(String[] args) {
		// Primitive type
		byte b1 = 100;
		short s1 = 200;
		char c1 = 'A';
		int i1 = 300;
		long l1 = 400;
		
		float f1 = 10.5f;
		double d1 = 10.5;
		
		boolean tf = true;
		
	}

}
