
public class LiteralExample {

	public static void main(String[] args) {
		int i1 = 10;  			// 정수 리터럴
		
		i1 = i1 + 25; 			// 25 정수 리터럴
		
		char ch = '가'; 			// 문자 리터럴
		
		double d = 3.14; 		// 실수 리터럴
		
		boolean tf = true; 		// 논리 리터럴
		
		String str = "Korea"; 	// 문자열 리터럴
		
		// 리터럴 : 소스 코드 내에서 직접 입력된 값.
	}

}
